## Mostly used Built-in functions in the Python
#1. Range Function
for num in range(10):
    print(num)      # This generates sequence of numbers in the form of list (0-9)

for num in range(3,11):
    print(num)      # This generates sequence of numbers in the form of list (3-10)

for num in range(3,11,2):
    print(num)      # This generates sequence of numbers in the form of list (3,5,7,9)
                    # The 2 is the difference between the numbers


